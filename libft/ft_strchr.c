#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	c = (unsigned char)c;
	while (*str)
	{
		if (*str == c)
			return ((char *)str);
		str++;
	}
	if (c == 0)
		return ((char *)str);
	return (0);
}
